package com.vairiscw.wssandroid.view.environment_page;

public class SmallEnvironmentPage {
    private int image;
    private String text;

    public SmallEnvironmentPage(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public String getText() {
        return text;
    }
}
